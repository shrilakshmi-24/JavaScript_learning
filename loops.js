//for-loop
for(var num=0;num<11;num+=2)
    console.log(num);

//for each loop
var a=[1,2,3,4,5];
a.forEach(item => console.log(item));

for(var item in a)
    console.log(item);

for(var item of a)
    console.log(item);

//while loop
var i=0;
while(i<5){
    console.log("value of i:"+i);
    i++;
}

//do-while
var i=0;
do{
    console.log("value of i:"+i);
    i++;
    
}while(i<0);



    


