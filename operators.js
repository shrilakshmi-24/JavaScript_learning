//post-increment
var itr=10;
console.log(itr++);//10
console.log(itr);

//pre-increment
console.log(++itr);//12
console.log(itr)//12

//post-decrement
console.log(itr--);//12
console.log(itr)//11

//pre-decrement
console.log(--itr);//10
console.log(itr)//10



//Shift operators
var a=8, b=2;
//left-shift
console.log(a<<b);//mul by4

//right-shift
console.log(a>>b);//divide by 4

